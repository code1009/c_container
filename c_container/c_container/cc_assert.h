#define cc_assert(x) \
	do                                                                                         \
	{                                                                                          \
		if (!(x))                                                                              \
		{                                                                                      \
			cc_assert_printf("assertion failed: %s (%s:%d)\n", #x, __FILE__, __LINE__);        \
		}                                                                                      \
	}                                                                                          \
	while(0)





/////////////////////////////////////////////////////////////////////////////
//===========================================================================
